// Variable Declaration

const firstName = 'Prashant';
const company = 'OpenAI';
let experience = 4.5;
let isAutomationEngineer = true;

// Printing Variables

console.log(firstName);
console.log(company);
console.log(experience);
console.log(isAutomationEngineer);

// Updating Variable

experience = 5;

console.log('Updated Experience:', experience);

// Multiple Variables

const browser = 'Chromium';
const baseUrl = 'https://example.com';
const timeout = 30000;

console.log(browser);
console.log(baseUrl);
console.log(timeout);
